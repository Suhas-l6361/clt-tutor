/**
 * Lists / normalizes rank_student photos and writes JS data for the homepage strip.
 * Run:
 *   node image/rank_student/build-rank-students.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const filesDir = path.resolve(__dirname, '../..');
const outJs = path.join(filesDir, 'js', 'nls-rank-holders.js');

const IMAGE_RE = /\.(jpe?g|png|webp)$/i;

function titleFromFile(filename) {
  let base = filename.replace(IMAGE_RE, '');
  // 01-divya-garg → Divya Garg
  base = base.replace(/^\d+[-_.\s]+/, '');
  // strip trailing year (2022-27) or ( 2016-21)
  base = base.replace(/\s*\([^)]*\)\s*$/, '');
  base = base.replace(/[-_]+/g, ' ').replace(/\s+/g, ' ').trim();
  if (!base) return filename;
  return base
    .split(' ')
    .map((w) => {
      if (/^[A-Z]{2,}$/.test(w) && w.length <= 4) return w; // MV, AKASH short allcaps keep
      if (w.includes('.')) return w; // M.V.
      return w.charAt(0).toUpperCase() + w.slice(1).toLowerCase();
    })
    .join(' ');
}

function safeSlug(filename) {
  const ext = path.extname(filename).toLowerCase();
  let base = path.basename(filename, path.extname(filename));
  const numMatch = base.match(/^(\d+)/);
  const num = numMatch ? String(parseInt(numMatch[1], 10)).padStart(2, '0') : 'xx';
  let rest = base.replace(/^\d+[-_.\s]*/, '');
  rest = rest.replace(/\s*\([^)]*\)\s*$/, '');
  rest = rest
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
  if (!rest) rest = 'student';
  return `${num}-${rest}${ext === '.jpeg' ? '.jpeg' : ext}`;
}

const all = fs
  .readdirSync(__dirname)
  .filter((f) => IMAGE_RE.test(f) && !f.startsWith('_'));

const renamed = [];
for (const file of all) {
  const dest = safeSlug(file);
  const srcPath = path.join(__dirname, file);
  const destPath = path.join(__dirname, dest);
  if (file !== dest) {
    if (fs.existsSync(destPath) && path.resolve(srcPath) !== path.resolve(destPath)) {
      // keep existing clean name; skip overwrite
      if (file.match(/^\d{2}-[a-z0-9-]+\./i)) {
        renamed.push(file);
        continue;
      }
      // if dest taken by different file, use unique
      let i = 2;
      let alt = dest.replace(IMAGE_RE, `-${i}$&`);
      while (fs.existsSync(path.join(__dirname, alt))) {
        i += 1;
        alt = dest.replace(IMAGE_RE, `-${i}$&`);
      }
      fs.renameSync(srcPath, path.join(__dirname, alt));
      renamed.push(alt);
      console.log('renamed', file, '->', alt);
      continue;
    }
    fs.renameSync(srcPath, destPath);
    console.log('renamed', file, '->', dest);
    renamed.push(dest);
  } else {
    renamed.push(file);
  }
}

const finalFiles = fs
  .readdirSync(__dirname)
  .filter((f) => IMAGE_RE.test(f) && !f.startsWith('_'))
  .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

const students = finalFiles.map((file) => ({
  file: 'image/rank_student/' + file,
  name: titleFromFile(file),
  school: 'NLS Bangalore',
}));

const js =
  '/** Auto-generated by image/rank_student/build-rank-students.mjs — do not edit by hand */\n' +
  'window.NLS_RANK_HOLDERS = ' +
  JSON.stringify(students, null, 2) +
  ';\n';

fs.writeFileSync(outJs, js, 'utf8');
fs.writeFileSync(path.join(__dirname, '_file-list.json'), JSON.stringify(finalFiles, null, 2), 'utf8');
console.log('Wrote', outJs);
console.log('Students:', students.length);
students.forEach((s, i) => console.log(String(i + 1).padStart(2, '0'), s.name, '←', s.file));
